console.log("<<<<< Pop-up Boxes >>>>>");

// There are three types of pop-up boxes in javascript: confirm, alert, and prompt.
confirm("Hi Panha JS");
alert("Hi Panha JS");
prompt("How are you Panha JS");

/*

    >   Confirm boxes will return "true" if ok is selected, and return "false" if cancel is selected. 
    >   Alert boxes will not return anything. 
    >   Prompt boxes will return whatever is in the text box. 
        Note: prompt boxes also have an optional second parameter, 
            which is the text that will already be in the text box.

*/