// Variables and Types

const myNumber = 3;                   // a number
const myString = "Hello, World!"      // a string
const myBoolean = true;               // a boolean

// In JavaScript, the Number type can be both a floating point number and an integer.
// Boolean variables can only be equal to either true or false.

// There are two more advanced types in JavaScript. An array, and an object.
const myArr = [];                    // an array
const myObj = {};                   // an object
// On top of that, there are two special types called undefined and null.

// When a variable is used without first defining a value for it, it is equal to undefined.
// However, the null value is a different type of value, and is used when a variable should be marked as empty. 
